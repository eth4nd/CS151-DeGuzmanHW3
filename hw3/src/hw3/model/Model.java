package hw3.model;

public class Model {
    private int red;
    private int green;
    private int blue;

    public Model(){
        red = 0;
        green = 0;
        blue = 0;
    }

    public void setRed(int red)
    {
        this.red = red;
    }

    public void setGreen(int green)
    {
        this.green = green;
    }

    public void setBlue(int blue)
    {
        this.blue = blue;
    }

    public int getRed()
    {
        return red;
    }

    public int getGreen()
    {
        return green;
    }

    public int getBlue()
    {
        return blue;
    }

}
